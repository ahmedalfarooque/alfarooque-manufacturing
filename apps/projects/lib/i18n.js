function applyDocumentLang(lang) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

const LOCALE = { en: 'en-US', ar: 'ar-SA' };

function formatDateFor(lang, value, opts) {
  if (!value) return '';
  const d = value instanceof Date ? value : new Date(value);
  if (isNaN(d.getTime())) return '';
  const locale = LOCALE[lang] || LOCALE.en;
  // toLocaleDateString doesn't accept timeStyle — only toLocaleString does.
  if (opts && opts.timeStyle) return d.toLocaleString(locale, opts);
  return d.toLocaleDateString(locale, opts);
}

function formatNumberFor(lang, value, opts) {
  if (value === null || value === undefined || value === '') return '';
  const n = Number(value);
  if (isNaN(n)) return String(value);
  return n.toLocaleString(LOCALE[lang] || LOCALE.en, opts);
}

const LanguageContext = createContext({
  lang: 'en',
  setLang: () => {},
  t: key => key,
  formatDate: (v, opts) => formatDateFor('en', v, opts),
  formatNumber: (v, opts) => formatNumberFor('en', v, opts),
});

export function LanguageProvider({ children }) {
  const [lang, setLangState] = useState('en');

  useEffect(() => {
    try {
      /* Cross-app pref cookie wins (latest choice made in ANY app);
         the per-app localStorage key stays as the fallback. */
      const saved = readPref(LANG_PREF_COOKIE) || localStorage.getItem(STORAGE_KEY);
      if (saved === 'ar' || saved === 'en') {
        setLangState(saved);
        applyDocumentLang(saved);
      } else {
        applyDocumentLang('en');
      }
    } catch (_) {
      applyDocumentLang('en');
    }
  }, []);

  const setLang = useCallback(next => {
    setLangState(next);
    applyDocumentLang(next);
    try { localStorage.setItem(STORAGE_KEY, next); } catch (_) {}
    writePref(LANG_PREF_COOKIE, next);
  }, []);

  const t = useCallback((key, vars) => {
    let str = (translations[lang] && translations[lang][key]) || translations.en[key] || key;
    if (vars) {
      Object.keys(vars).forEach(k => {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
      });
    }
    return str;
  }, [lang]);

  const formatDate = useCallback((value, opts) => formatDateFor(lang, value, opts), [lang]);
  const formatNumber = useCallback((value, opts) => formatNumberFor(lang, value, opts), [lang]);

  const value = useMemo(() => ({ lang, setLang, t, formatDate, formatNumber }), [lang, setLang, t, formatDate, formatNumber]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  return useContext(LanguageContext);
}

/* Display helper for DB enum values (statuses, priorities, order stages…):
   the stored value stays English; this shows its translation when one
   exists under `<prefix>.<value>` and falls back to the raw value —
   never to a bare dictionary key — so unknown/new enum values still
   render something sensible instead of "status.Foo". */
export function trEnum(t, prefix, value) {
  if (value === null || value === undefined || value === '') return value;
  const key = prefix + '.' + value;
  const s = t(key);
  return s === key ? String(value) : s;
}
